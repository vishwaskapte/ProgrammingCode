using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ProgrammingCode
{
    public class SumOfNaturalNumbers
    {

        // Formula is Sum = N * (N + 1) / 2

        public int SumOfNumbers(int n)
        {
            return n * (n + 1) / 2;
        }
    }
}
